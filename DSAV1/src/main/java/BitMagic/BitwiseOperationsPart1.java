package BitMagic;

public class BitwiseOperationsPart1 {

    public static void main(String[] args) {

        int x =3 ;
        int y = 6 ;
        //and operations
        System.out.println( x & y);
        //bitwise or operations
        System.out.println(x | y );
        //bitwise Bitwise Xor
        System.out.println(x ^ y);
        //bitwise not operator
        int a = 1;
        System.out.println(~a);

    }
}
